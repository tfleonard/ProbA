#ifndef MYTYPES_H
#define	MYTYPES_H


#ifndef uint32
typedef unsigned long uint32;
#endif

#ifndef int32
typedef long int int32;
#endif

#ifndef uint16
typedef unsigned short uint16;
#endif

#ifndef int16
typedef short int int16;
#endif

#ifndef uint8
typedef unsigned char uint8;
#endif

#ifndef int8
typedef char int8;
#endif

#endif	/* MYTYPES_H */

